def OneToTen():
    sum = 0
    for i in range(1, 10):
        sum += i
    return sum
def TwentytoThridy():
    sum = 0
    for i in range(20, 37):
        sum += i
    return sum
def ThirtyFivetoFourtyNine():
    sum = 0
    for i in range(35, 45):
        sum += i
    return sum

result1 = OneToTen()
print(result1)
result2 = TwentytoThridy()
print(result2)
result3 = ThirtyFivetoFourtyNine()
print(result3)

